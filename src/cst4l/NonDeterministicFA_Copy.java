    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        String criteria = (String) cmbCriteria.getSelectedItem();
        inputString = tfInput.getText().trim();
        
        if (inputString.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Input cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (!inputString.matches("[01]+")) {
            JOptionPane.showMessageDialog(this, "Input must be a binary string (0s and 1s).", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Reset Transition Table
        DefaultTableModel model = (DefaultTableModel)tblTransition.getModel();
        model.setRowCount(0);
        
        initializeTransitions(criteria);
        updateTransitionTable();
        simulateNDFA(taOutput, criteria);
    }//GEN-LAST:event_btnValidateActionPerformed

