    private void initializeTransitions(String criteria) {
        transitions = switch (criteria) {
            case "Ends with '11'" -> getEndsWith11Transitions();
            case "Contains '110'" -> getContains110Transitions();
            case "Ends with '01'" -> getEndsWith01Transitions();
            default -> new HashMap<>();
        };
    }

    private Map<Integer, Map<String, Set<Integer>>> getEndsWith11Transitions() {
        Map<Integer, Map<String, Set<Integer>>> map = new HashMap<>();

        map.put(0, Map.of("1", Set.of(1), "0", Set.of(0)));
        map.put(1, Map.of("1", Set.of(2), "0", Set.of(0)));
        map.put(2, Map.of("1", Set.of(2), "0", Set.of(0)));
        map.put(DEAD_STATE, Map.of("1", Set.of(DEAD_STATE), "0", Set.of(DEAD_STATE)));

        return map;
    }

    private Map<Integer, Map<String, Set<Integer>>> getContains110Transitions() {
        Map<Integer, Map<String, Set<Integer>>> map = new HashMap<>();

        map.put(0, Map.of("0", Set.of(0), "1", Set.of(1)));
        map.put(1, Map.of("0", Set.of(2), "1", Set.of(1)));
        map.put(2, Map.of("0", Set.of(2), "1", Set.of(3)));
        map.put(3, Map.of("0", Set.of(3), "1", Set.of(3)));
        map.put(DEAD_STATE, Map.of("0", Set.of(DEAD_STATE), "1", Set.of(DEAD_STATE)));

        return map;
    }

    private Map<Integer, Map<String, Set<Integer>>> getEndsWith01Transitions() {
        Map<Integer, Map<String, Set<Integer>>> map = new HashMap<>();

        map.put(0, Map.of("0", Set.of(0), "1", Set.of(1)));
        map.put(1, Map.of("0", Set.of(2), "1", Set.of(1)));
        map.put(2, Map.of("0", Set.of(0), "1", Set.of(1)));
        map.put(DEAD_STATE, Map.of("0", Set.of(DEAD_STATE), "1", Set.of(DEAD_STATE)));

        return map;
    }
    
    private void updateTransitionTable() {
        DefaultTableModel model = (DefaultTableModel) tblTransition.getModel();
        model.setRowCount(0);  // Clear existing rows

        // State labels for each state
        String[] stateLabels = {"q0", "q1", "q2", "qd"};  // Adjust if you have more states

        // Iterate over the states and update the transition table
        for (int i = 0; i < stateLabels.length; i++) {
            Map<String, Set<Integer>> transition = transitions.getOrDefault(i, transitions.get(DEAD_STATE));

            // Get all possible transitions for "0" and "1"
            Set<Integer> nextStatesOn0 = transition.getOrDefault("0", Set.of(DEAD_STATE));
            Set<Integer> nextStatesOn1 = transition.getOrDefault("1", Set.of(DEAD_STATE));

            // Convert state numbers to state labels
            String transitionOn0 = nextStatesOn0.stream()
                .map(state -> state == DEAD_STATE ? "qd" : "q" + state)
                .collect(Collectors.joining(", "));

            String transitionOn1 = nextStatesOn1.stream()
                .map(state -> state == DEAD_STATE ? "qd" : "q" + state)
                .collect(Collectors.joining(", "));

            // Add row to the table with formatted state transitions
            model.addRow(new Object[]{
                stateLabels[i],  // State label (e.g., q0, q1, etc.)
                transitionOn0,   // Transitions for symbol "0"
                transitionOn1    // Transitions for symbol "1"
            });
        }
    }




    private void simulateNDFA(JTextArea taOutput, String criteria) {
        inputIndex = 0;
        final Set<Integer>[] currentStates = new Set[]{new HashSet<>()};
        currentStates[0].add(0); // Initial state as a set

        taOutput.setText("Starting NDFA Simulation...\n");
        log(taOutput, "Input String: " + inputString);
        log(taOutput, "Criteria: " + criteria);
        log(taOutput, "Initial States: " + currentStates[0]);

        Timer timer = new Timer(1000, new AbstractAction() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if (inputIndex < inputString.length()) {
                    String symbol = String.valueOf(inputString.charAt(inputIndex));
                    Set<Integer> nextStates = new HashSet<>();
                    for (int state : currentStates[0]) {
                        nextStates.addAll(transitions
                            .getOrDefault(state, transitions.get(DEAD_STATE))
                            .getOrDefault(symbol, Set.of(DEAD_STATE))
                        );
                    }
                    currentStates[0] = nextStates; // Mutable wrapper allows reassignment
                    log(taOutput, "After reading symbol '" + symbol + "': " + currentStates[0]);
                    inputIndex++;
                } else {
                    ((Timer) e.getSource()).stop();
                    finalizeNDFA(currentStates[0], criteria, taOutput);
                }
            }
        });
        timer.start();
    }

    private void finalizeNDFA(Set<Integer> currentStates, String criteria, JTextArea taOutput) {
        boolean accepted = switch (criteria) {
            case "Ends with '11'" -> currentStates.contains(2);
            case "Contains '110'" -> currentStates.contains(3);
            case "Ends with '01'" -> currentStates.contains(2);
            default -> false;
        };

        String result = accepted ? "String Accepted!" : "String Rejected!";
        log(taOutput, result);
        JOptionPane.showMessageDialog(this, result, "Result", JOptionPane.INFORMATION_MESSAGE);
    }
    
    private void log(JTextArea taOutput, String message) {
        taOutput.append(message + "\n");
    }
    
