    public static boolean validateString(String input, String productionRule) {
        switch (productionRule) {
            case "S -> aSb":
                return validateASb(input); 
            case "S -> aSa | bSb | ε":
                return validateASaBSbEpsilon(input);
            case "S -> aSa | bSb | c":
                return validateASaBSbC(input);
            default:
                return false;
        }
    }
    
    public static boolean validateASb(String str) {
        if (str.isEmpty()) {
            return true; // Base case for ε
        }

        if (str.length() < 2) {
            return false; // Reject strings like "a" or "b" or invalid lengths
        }

        char first = str.charAt(0);
        char last = str.charAt(str.length() - 1);

        // Ensure the first and last characters are 'a' and 'b' respectively
        if (first == 'a' && last == 'b') {
            return validateASb(str.substring(1, str.length() - 1)); // Recursive check
        }

        return false; // Reject if it doesn't match the pattern
    }
    
    public static boolean validateASaBSbEpsilon(String str) {
        if (str.isEmpty()) {
            return true; // ε case
        }

        if (str.length() < 2) {
            return false; // Strings like "a" or "b" cannot be valid
        }

        char first = str.charAt(0);
        char last = str.charAt(str.length() - 1);

        if ((first == 'a' && last == 'a') || (first == 'b' && last == 'b')) {
            return validateASaBSbEpsilon(str.substring(1, str.length() - 1));
        }

        return false; // Reject if characters do not match the rule
    }

    public static boolean validateASaBSbC(String str) {
        if (str.isEmpty()) {
            return false;
        }

        if (str.equals("c")) {
            return true;
        }

        if (str.length() < 3) {
            return false;
        }

        char first = str.charAt(0);
        char last = str.charAt(str.length() - 1);

        if ((first == 'a' && last == 'a') || (first == 'b' && last == 'b')) {
            return validateASaBSbC(str.substring(1, str.length() - 1));
        }

        return false;
    }
    
