    private void btnValidateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnValidateActionPerformed
        String result = "";
        String input = tfInput.getText().trim();
        String selectedRule = (String) cmbProdRule.getSelectedItem();

        if (selectedRule == null || selectedRule.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select a Product Rule.",
                    "No Production Rule Selected", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a string.",
                    "No String Entered", JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean isValid = validateString(input, selectedRule);
        if (isValid) {
            result += "Input: " + input + "\n";
            result += "String is Accepted!";
            taResult.setText(result);
        } else {
            result += "Input: " + input + "\n";
            result += "String is Rejeced!";
            taResult.setText(result);
        }
    }//GEN-LAST:event_btnValidateActionPerformed

